package cn.nuaa.ai.entity;

public class Aps {
	private int Id;
	private String EpIds;
	private String Freqs;

	public int getId() {
		return Id;
	}

	public void setId(int id) {
		Id = id;
	}

	public String getEpIds() {
		return EpIds;
	}

	public void setEpIds(String epIds) {
		EpIds = epIds;
	}

	public String getFreqs() {
		return Freqs;
	}

	public void setFreqs(String freqs) {
		Freqs = freqs;
	}

}
